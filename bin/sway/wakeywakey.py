#!/usr/bin/env python3
"""
Wakeywakey

A simple systray icon that can toggle a systemd inhibitor.

The intent of this script is to provide a way to have an external display
connected to a docked laptop in clamshell mode (lid closed), but have the
laptop avoid going to sleep even if the external display is shut off.

By default, systemd-logind will put the laptop to sleep in this scenario. Using
swayidle as a lock inhibitor will work, but then the external display will
never go to sleep, thus wasting power and backlight lifetime.

You can also simply run `systemd-inhibit` as a background task, but then the
system will _never_ go to sleep, even if it is disconnected from the dock and
the external display.

Requirements:
    - pillow
    - pystray
"""
import sys
import subprocess
import io
import base64

from pystray import Icon, Menu, MenuItem
from PIL import Image

image_disabled = None
image_enabled = None

# Is the inhibitor enabled?
enabled = False
# The process object for our inhibitor process.
proc = None


def start_inhibitor():
    global proc
    cmd = ['systemd-inhibit',
           '--what=sleep:handle-lid-switch',
           'sleep', '365d']
    proc = subprocess.Popen(cmd)


def stop_inhibitor():
    proc.kill()
    proc.wait()


def action(icon, query):
    # The `query` string can be used to handle multiple actions, as dictated by
    # the menu item clicked on. In our case, we don't need a menu or multiple
    # actions, so it is ignored.
    global enabled

    if enabled:
        enabled = False
        icon.icon = image_disabled
        stop_inhibitor()
    else:
        enabled = True
        icon.icon = image_enabled
        start_inhibitor()


def create_image(b64):
    buf = io.BytesIO(base64.b64decode(b64))
    return Image.open(buf)


def main():
    global image_disabled, image_enabled
    image_disabled = create_image(B64_IMG_DISABLED)
    image_enabled = create_image(B64_IMG_ENABLED)

    menu = Menu(MenuItem('Toggle', action, default=True))
    icon = Icon(name='Wakey', icon=image_disabled, title='Wakey', menu=menu)
    icon.run()


